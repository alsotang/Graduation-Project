       		});
       		if($(this).hasClass("on"))
       		{
       			$(".login").slideUp("fast",function(){
	       			$(this).remove();
	       			flag = true;
	       		});
       			$(this).removeClass("on");
       		}
       		else
       		{
	       		$(this).addClass("on");
       		}
       	});

       	$(".post .tag-del-cont").mouseenter(function(){
       		if($(this).hasClass("active"))
       		{
       			$(this).parent().siblings(".tag-label").children(".tag-label-del-recall").show();
