			$(this).parent().siblings(".tag-label").children(".tag-label-del").hide();
   			$(this).parent().siblings(".tag-label").children(".tag-label-del-recall").hide();
    		$(this).children(".tag-del-dark").removeClass("on");
       	});
       	$(".post .tag-del-cont").click(function(){

       		if(flag)
